// Fallback to English if translation not found, or use Google Translate API
export async function getTranslation(languageCode, key) {
  const lang = translations[languageCode];

  // If we have the translation, return it
  if (lang && lang[key]) {
    return lang[key];
  }

  // If we have English translation, use it as fallback
  const englishText = translations.en[key] || key;

  // If target language is English or we don't have Google Translate API, return English
  if (languageCode === 'en') {
    return englishText;
  }

  // For languages without pre-defined translations, use Google Translate API
  // This happens on the client-side, so we return the English text for now
  // The actual translation will be handled by the useTranslation hook
  return englishText;
}

// Synchronous version for immediate use (returns English if translation not found)
export function getTranslationSync(languageCode, key) {
  const lang = translations[languageCode] || translations.en;
  return lang[key] || translations.en[key] || key;
}
